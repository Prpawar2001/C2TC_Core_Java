class Def_val{

	int a;
	char b;
	short c;
	boolean d;
	long e;
	float y;
	double z;
}
public class Default_values {

	public static void main(String[] args) {
		
		Def_val dv = new Def_val();
		
		System.out.println("this is int " + dv.a);
		System.out.println("this is char " + dv.b);
		System.out.println("this is short " + dv.c);
		System.out.println("this is boolean " + dv.d);
		System.out.println("this is long " + dv.e);
		System.out.println("this is float " + dv.y);
		System.out.println("this is double " + dv.z);

	}

}
